            <h2>UNCESCO</h2>
<p>
A World Heritage Site is a landmark or area with legal protection by an international convention administered by the <a class="text-decoration" href="https://whc.unesco.org/" style="color:white"> United Nations Educational, Scientific and Cultural Organization (UNESCO)</a>.
</p>

<p>
World Heritage Sites are designated by UNESCO for having cultural, historical, scientific or other form of significance. </p><p> Sites are judged to contain "cultural and natural heritage around the world considered to be of outstanding value to humanity".
			</p>
